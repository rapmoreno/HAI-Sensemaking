"use strict";

/**
 * Module: effect.report.js
 * Type: Effect module
 * Purpose: Report page — fetch report from backend, render sections
 *
 * Depends on:
 *   - effect.error.js (error handling)
 *   - effect.export.js (PDF export)
 *   - config.js (API_BASE)
 *
 * Used by: main.js
 * Side effects: DOM writes, HTTP requests, sessionStorage reads
 */

import { handleError } from "./effect.error.js";
import { initExport } from "./effect.export.js";
import { CONFIG } from "./config.js";

let _canvasState = null;
let _reportData = null;

function initReport() {
  _canvasState = _loadCanvasState();

  if (!_canvasState) {
    _showError("No canvas data found. Please go back and analyse a canvas first.");
    return;
  }

  const btnRetry = document.getElementById("btn-retry");
  if (btnRetry) {
    btnRetry.addEventListener("click", () => _fetchReport());
  }

  _fetchReport();
}

function _loadCanvasState() {
  try {
    const raw = sessionStorage.getItem("hai_canvas_state");
    return raw ? JSON.parse(raw) : null;
  } catch {
    return null;
  }
}

async function _fetchReport() {
  _showLoading();

  const url = `${CONFIG.API_BASE}/report`;

  try {
    const res = await fetch(url, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ canvas_state: _canvasState }),
    });

    if (!res.ok) {
      const detail = await res.json().catch(() => ({ message: res.statusText }));
      throw new Error(detail.detail?.message || detail.message || `HTTP ${res.status}`);
    }

    _reportData = await res.json();
    _renderReport(_reportData);
    _showContent();

    initExport(_canvasState, _reportData);
  } catch (error) {
    handleError("effect.report", "_fetchReport", error);
    _showError(error.message);
  }
}

function _renderReport(report) {
  const title = document.getElementById("report-title");
  const meta = document.getElementById("report-meta");
  const execSummary = document.getElementById("report-exec-summary");
  const overview = document.getElementById("report-overview");
  const stepsBody = document.getElementById("report-steps-body");
  const risks = document.getElementById("report-risks");
  const recs = document.getElementById("report-recs");
  const trust = document.getElementById("report-trust");

  if (title) title.textContent = report.title || "Untitled Report";

  if (meta) {
    const contextType = _canvasState.context_type || "";
    const today = new Date().toLocaleDateString("en-GB", {
      day: "numeric",
      month: "long",
      year: "numeric",
    });
    meta.textContent = `${contextType} \u00b7 ${today} \u00b7 Generated by HAI Canvas`;
  }

  if (execSummary) execSummary.textContent = report.executive_summary || "";
  if (overview) overview.textContent = report.process_overview || "";
  if (trust) trust.textContent = report.trust_summary || "";

  if (stepsBody) {
    stepsBody.replaceChildren();
    const steps = report.step_analyses || [];
    steps.forEach((sa) => {
      const mainRow = document.createElement("tr");
      const cells = [
        sa.title || "",
        sa.task_allocation || "",
        sa.risk_level || "",
        sa.trust_requirement || "",
        sa.handoff_needed || "",
      ];
      cells.forEach((text, i) => {
        const td = document.createElement("td");
        if (i === 0) td.className = "step-row__title";
        td.textContent = text;
        mainRow.appendChild(td);
      });
      stepsBody.appendChild(mainRow);

      if (sa.summary) {
        const summaryRow = document.createElement("tr");
        const td = document.createElement("td");
        td.colSpan = 5;
        td.className = "step-row__summary";
        td.textContent = sa.summary;
        summaryRow.appendChild(td);
        stepsBody.appendChild(summaryRow);
      }
    });
  }

  if (risks) {
    risks.replaceChildren();
    (report.key_risks || []).forEach((r) => {
      const li = document.createElement("li");
      li.textContent = r;
      risks.appendChild(li);
    });
  }

  if (recs) {
    recs.replaceChildren();
    (report.recommendations || []).forEach((r) => {
      const li = document.createElement("li");
      li.textContent = r;
      recs.appendChild(li);
    });
  }
}

function _showLoading() {
  _toggle("report-loading", true);
  _toggle("report-error", false);
  _toggle("report-content", false);
}

function _showContent() {
  _toggle("report-loading", false);
  _toggle("report-error", false);
  _toggle("report-content", true);
}

function _showError(message) {
  _toggle("report-loading", false);
  _toggle("report-error", true);
  _toggle("report-content", false);

  const text = document.querySelector(".report-error__text");
  if (text) text.textContent = message || "Failed to generate report.";
}

function _toggle(id, show) {
  const el = document.getElementById(id);
  if (!el) return;
  el.hidden = !show;
}

export { initReport };
